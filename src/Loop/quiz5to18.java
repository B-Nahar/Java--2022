package Loop;

public class quiz5to18 {
    //Straing;Condition;Inc/Drc

    public static void main(String[]args){

        int a= -5;
        while(a<=18){
            System.out.println(a);
            a++;
        }
        // -5 to 18 = for loop

        for(int b=-5;b<=18;b++){
            System.out.println(b);
        }

    }
}
