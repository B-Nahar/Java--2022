package Feb9;

public class Print23to29 {

    public static void main(String [] args){

        int a =23;
        while( a<=29){
            System.out.println(a);
            a++;
        }
        //23 to 29 for loop


        for(int i =23; i<=29;i++){
            System.out.println(i);
        }


    }

}
